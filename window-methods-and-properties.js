// List of Window Methods and Properties in JavaScript

window.close(); // Closes the current window
window.open(); // Opens a new browser window
window.location.reload(); // Reloads the current page
window.alert("Hello!"); // Displays an alert dialog
window.confirm("Are you sure?"); // Displays a confirmation dialog
window.prompt("Enter your name:"); // Displays a prompt dialog
window.setTimeout(() => {}, 1000); // Sets a timer that executes a function after a specified delay
window.setInterval(() => {}, 1000); // Repeatedly calls a function at specified intervals
window.clearTimeout(); // Cancels a timeout set with setTimeout
window.clearInterval(); // Cancels an interval set with setInterval
window.scrollTo(0, 0); // Scrolls to a specified position in the document
window.print(); // Opens the Print dialog
window.history.back(); // Navigates to the previous page in the history
window.history.forward(); // Navigates to the next page in the history
window.history.go(-1); // Moves backward in the history by a specified number of pages
window.focus(); // Brings the window to the front
window.blur(); // Removes focus from the window
window.open('https://example.com'); // Opens a URL in a new window
window.resizeTo(800, 600); // Resizes the window to specified width and height
window.moveTo(100, 100); // Moves the window to specified coordinates
window.localStorage.setItem('key', 'value'); // Sets a value in local storage
window.localStorage.getItem('key'); // Retrieves a value from local storage
window.sessionStorage.setItem('key', 'value'); // Sets a value in session storage
window.sessionStorage.getItem('key'); // Retrieves a value from session storage
window.navigator.userAgent; // Returns the user agent string for the browser
window.screen.width; // Returns the width of the screen
window.screen.height; // Returns the height of the screen
window.screenX; // Returns the horizontal coordinate of the window
window.screenY; // Returns the vertical coordinate of the window
